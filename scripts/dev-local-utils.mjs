import crypto from "crypto";
import { existsSync, readFileSync } from "fs";
import { isAbsolute, resolve } from "path";

const PLACEHOLDER_VALUES = new Set(["", "<auto-generated>", "\"<auto-generated>\"", "'<auto-generated>'"]);

export function parseOptions(rawArgs) {
  const parsed = {};
  for (let i = 0; i < rawArgs.length; i++) {
    const arg = rawArgs[i];
    if (!arg.startsWith("--")) continue;
    const withoutPrefix = arg.slice(2);
    const equalsIndex = withoutPrefix.indexOf("=");
    if (equalsIndex >= 0) {
      parsed[withoutPrefix.slice(0, equalsIndex)] = withoutPrefix.slice(equalsIndex + 1);
      continue;
    }

    const next = rawArgs[i + 1];
    if (next && !next.startsWith("--")) {
      parsed[withoutPrefix] = next;
      i++;
    } else {
      parsed[withoutPrefix] = true;
    }
  }
  return parsed;
}

export function assertStringOptions(options, optionNames) {
  for (const name of optionNames) {
    if (options[name] === true) {
      throw new Error(`Option --${name} requires a value.`);
    }
  }
}

export function getArgValue(args, name) {
  const inline = args.find((arg) => arg.startsWith(`${name}=`));
  if (inline) return inline.slice(name.length + 1);
  const index = args.indexOf(name);
  if (index >= 0) {
    const value = args[index + 1];
    if (!value || value.startsWith("--")) {
      throw new Error(`Option ${name} requires a value.`);
    }
    return value;
  }
  return undefined;
}

export function shellQuote(value) {
  return `'${String(value).replace(/'/g, `'\\''`)}'`;
}

export function trimTrailingSlash(value) {
  return value.replace(/\/+$/, "");
}

export function assertLocalUrl(value) {
  const url = new URL(value);
  if (!isLocalHostname(url.hostname)) {
    throw new Error(`Refusing to run against non-local API URL: ${value}`);
  }
}

export function collectLocalUrlConfigIssues(entries) {
  const issues = [];

  for (const entry of entries) {
    const value = entry.value;
    if (typeof value !== "string" || value.trim() === "" || value.trim() === "<auto-generated>") {
      continue;
    }

    const location = `${entry.label}:${entry.key}`;
    let url;
    try {
      url = new URL(value);
    } catch {
      issues.push(`${location} must be a valid local URL.`);
      continue;
    }

    if (url.protocol !== "http:") {
      issues.push(`${location} must use http for local dev.`);
    }

    if (!isLocalHostname(url.hostname)) {
      issues.push(`${location} must point to localhost, 127.0.0.1, or ::1.`);
    }

    if (entry.port && getUrlPort(url) !== String(entry.port)) {
      issues.push(`${location} must use port ${entry.port}.`);
    }

    if (entry.pathname !== undefined && normalizePathname(url.pathname) !== normalizePathname(entry.pathname)) {
      const expectedPath = normalizePathname(entry.pathname) || "/";
      issues.push(`${location} must use path ${expectedPath}.`);
    }
  }

  return issues;
}

export function assertPassword(password) {
  if (typeof password !== "string") {
    throw new Error("Local admin password must be provided as a string.");
  }
  if (password.length < 12) {
    throw new Error("Local admin password must be at least 12 characters.");
  }
}

export function resolveLocalStatePath(root, value) {
  if (!value) return resolve(root, ".wrangler", "state");
  return isAbsolute(value) ? value : resolve(root, value);
}

export function generateSecret() {
  return crypto.randomBytes(32).toString("base64");
}

export function parseEnvFileContent(content) {
  const values = {};
  for (const rawLine of content.split(/\r?\n/)) {
    let line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;
    if (line.startsWith("export ")) line = line.slice("export ".length).trim();

    const equalsIndex = line.indexOf("=");
    if (equalsIndex <= 0) continue;

    const key = line.slice(0, equalsIndex).trim();
    const rawValue = line.slice(equalsIndex + 1).trim();
    values[key] = normalizeEnvValue(rawValue);
  }
  return values;
}

export function readEnvVarsIfExists(filePath) {
  if (!filePath || !existsSync(filePath)) return null;
  return parseEnvFileContent(readFileSync(filePath, "utf8"));
}

export function resolveSharedLocalSecrets({
  forceRegenerate = false,
  apiVars = null,
  adminVars = null,
  storefrontVars = null,
  env = process.env,
} = {}) {
  const existingApi = forceRegenerate ? null : apiVars;
  const existingAdmin = forceRegenerate ? null : adminVars;
  const existingStorefront = forceRegenerate ? null : storefrontVars;

  return {
    betterAuthSecret: firstUsableValue(
      env.BETTER_AUTH_SECRET,
      existingApi?.BETTER_AUTH_SECRET,
      existingAdmin?.BETTER_AUTH_SECRET,
      generateSecret,
    ),
    jwtSecret: firstUsableValue(
      env.JWT_SECRET,
      existingApi?.JWT_SECRET,
      existingAdmin?.JWT_SECRET,
      existingStorefront?.JWT_SECRET,
      generateSecret,
    ),
    apiToken: firstUsableValue(
      env.API_TOKEN,
      existingApi?.API_TOKEN,
      existingAdmin?.API_TOKEN,
      existingStorefront?.API_TOKEN,
      generateSecret,
    ),
    purgeToken: firstUsableValue(
      env.PURGE_TOKEN,
      existingStorefront?.PURGE_TOKEN,
      existingApi?.PURGE_TOKEN,
      generateSecret,
    ),
    credentialEncryptionKey: firstUsableValue(
      env.CREDENTIAL_ENCRYPTION_KEY,
      existingApi?.CREDENTIAL_ENCRYPTION_KEY,
      existingAdmin?.CREDENTIAL_ENCRYPTION_KEY,
      generateSecret,
    ),
  };
}

export function collectLocalSecretSyncIssues({ apiVars, adminVars, storefrontVars }) {
  const files = [
    { name: "apps/api/.dev.vars", vars: apiVars },
    { name: "apps/admin-v2/.dev.vars", vars: adminVars },
    { name: "apps/storefront/.dev.vars", vars: storefrontVars },
  ].filter((file) => file.vars);

  const sharedSecrets = [
    { key: "BETTER_AUTH_SECRET", files: ["apps/api/.dev.vars", "apps/admin-v2/.dev.vars"] },
    { key: "JWT_SECRET", files: ["apps/api/.dev.vars", "apps/admin-v2/.dev.vars", "apps/storefront/.dev.vars"] },
    { key: "API_TOKEN", files: ["apps/api/.dev.vars", "apps/admin-v2/.dev.vars", "apps/storefront/.dev.vars"] },
    { key: "CREDENTIAL_ENCRYPTION_KEY", files: ["apps/api/.dev.vars", "apps/admin-v2/.dev.vars"] },
    {
      key: "PURGE_TOKEN",
      files: ["apps/api/.dev.vars", "apps/storefront/.dev.vars"],
      requiredFiles: ["apps/storefront/.dev.vars"],
    },
  ];

  const issues = [];
  for (const secret of sharedSecrets) {
    const relevantFiles = files.filter((file) => secret.files.includes(file.name));
    const requiredFiles = secret.requiredFiles ?? secret.files;
    const missing = relevantFiles
      .filter((file) => requiredFiles.includes(file.name))
      .filter((file) => !isUsableSecret(file.vars[secret.key]))
      .map((file) => file.name);
    if (missing.length > 0) {
      issues.push(`${secret.key} is missing from ${missing.join(", ")}`);
    }

    const valueOwners = new Map();
    for (const file of relevantFiles) {
      const value = file.vars[secret.key];
      if (!isUsableSecret(value)) continue;
      const owners = valueOwners.get(value) ?? [];
      owners.push(file.name);
      valueOwners.set(value, owners);
    }
    if (valueOwners.size > 1) {
      issues.push(`${secret.key} differs between local .dev.vars files`);
    }
  }
  return issues;
}

export function assertLocalSecretSync(envFiles) {
  const issues = collectLocalSecretSyncIssues(envFiles);
  if (issues.length === 0) return;

  throw new Error(
    [
      "Local .dev.vars shared secrets are out of sync.",
      ...issues.map((issue) => `- ${issue}`),
      "",
      "Run pnpm dev:setup --force --env-only to regenerate local env files without touching the database, or manually align the shared values.",
    ].join("\n"),
  );
}

function normalizeEnvValue(rawValue) {
  if (!rawValue) return "";
  const quote = rawValue[0];
  if ((quote === "\"" || quote === "'") && rawValue.at(-1) === quote) {
    return rawValue.slice(1, -1);
  }
  return rawValue.replace(/\s+#.*$/, "").trim();
}

function firstUsableValue(...candidates) {
  const fallback = candidates.at(-1);
  for (const candidate of candidates.slice(0, -1)) {
    if (isUsableSecret(candidate)) return candidate;
  }
  return typeof fallback === "function" ? fallback() : fallback;
}

function isUsableSecret(value) {
  return typeof value === "string" && !PLACEHOLDER_VALUES.has(value.trim());
}

function isLocalHostname(hostname) {
  const normalized = hostname.toLowerCase().replace(/^\[/, "").replace(/\]$/, "");
  return normalized === "localhost" || normalized === "127.0.0.1" || normalized === "::1";
}

function getUrlPort(url) {
  if (url.port) return url.port;
  if (url.protocol === "http:") return "80";
  if (url.protocol === "https:") return "443";
  return "";
}

function normalizePathname(pathname) {
  return pathname.replace(/\/+$/, "");
}
