#!/usr/bin/env node
/**
 * Generate Build ID for Cache Invalidation
 *
 * This script generates a deterministic BUILD_ID for cache key scoping.
 * Prefer a provider commit SHA; otherwise hash source/config inputs.
 *
 * How it works:
 * 1. Build/typecheck scripts run this before importing "@/config/build-id"
 * 2. Generates a commit/source-hash BUILD_ID
 * 3. Writes to src/config/build-id.ts
 * 4. Gets included in deployment
 */

import { fileURLToPath } from 'url';
import { dirname, join, relative } from 'path';
import { createHash } from 'crypto';
import { mkdirSync, writeFileSync, existsSync, readdirSync, readFileSync, statSync } from 'fs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const appRoot = join(__dirname, '..');
const repoRoot = join(appRoot, '..', '..');

const configDir = join(appRoot, 'src/config');
const outputFile = join(configDir, 'build-id.ts');
const ignoredRelativePaths = new Set([
  'src/config/build-id.ts',
  '.astro',
  'dist',
  'node_modules',
]);

function getCommitBuildId() {
  const sha = process.env.CF_PAGES_COMMIT_SHA
    || process.env.VERCEL_GIT_COMMIT_SHA
    || process.env.GITHUB_SHA
    || process.env.COMMIT_SHA;

  if (!sha) return null;
  return `commit-${sha.slice(0, 16)}`;
}

function collectFiles(dir, files = []) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const fullPath = join(dir, entry.name);
    const relativePath = relative(appRoot, fullPath).replaceAll('\\', '/');

    if (ignoredRelativePaths.has(relativePath) || entry.name === '.DS_Store') continue;
    if (entry.isDirectory()) {
      collectFiles(fullPath, files);
      continue;
    }
    if (entry.isFile()) files.push(fullPath);
  }
  return files;
}

function getSourceBuildId() {
  const hash = createHash('sha256');
  const roots = [
    join(appRoot, 'src'),
    join(appRoot, 'public'),
  ];
  const files = [];

  for (const root of roots) {
    if (existsSync(root)) collectFiles(root, files);
  }

  for (const file of [
    join(appRoot, 'astro.config.mjs'),
    join(appRoot, 'package.json'),
    join(appRoot, 'tsconfig.json'),
    join(appRoot, 'wrangler.jsonc'),
    join(repoRoot, 'pnpm-lock.yaml'),
  ]) {
    if (existsSync(file) && statSync(file).isFile()) files.push(file);
  }

  for (const file of files.sort()) {
    hash.update(relative(repoRoot, file).replaceAll('\\', '/'));
    hash.update('\0');
    hash.update(readFileSync(file));
    hash.update('\0');
  }

  return `src-${hash.digest('hex').slice(0, 16)}`;
}

const BUILD_ID = getCommitBuildId() ?? getSourceBuildId();

// Ensure config directory exists
if (!existsSync(configDir)) {
  mkdirSync(configDir, { recursive: true });
}

const content = `/**
 * Auto-generated Build ID
 *
 * DO NOT EDIT THIS FILE MANUALLY
 * This file is auto-generated by scripts/generate-build-id.js
 */

export const BUILD_ID = "${BUILD_ID}";
`;

writeFileSync(outputFile, content, 'utf-8');

console.log(`✓ Build ID generated: ${BUILD_ID}`);
console.log(`   Written to: ${outputFile}`);
